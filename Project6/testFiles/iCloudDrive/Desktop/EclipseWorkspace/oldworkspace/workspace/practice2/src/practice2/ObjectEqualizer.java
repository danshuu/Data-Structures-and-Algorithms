package practice2;

public class ObjectEqualizer {
	//instance variables
	private int x;
	private String y;
	private double z;
	
	//constructors
	public ObjectEqualizer() {
		this.x = 0;
		this.y = "none";
		this.z = 1.0;
	}
	public ObjectEqualizer(int x, String y, double z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
}
